#!/usr/bin/env bash
set -euo pipefail

# Usage: ./automate.sh [audit|features|deploy|all]   (default: all)
# Env toggles: DEPLOY_REMOTE=1 REMOTE_HOST=4 REMOTE_PATH=/srv/<name>

REPO="/Applications/my_projects/dataalgebra_AI_POC/project-burnside-www-production"
